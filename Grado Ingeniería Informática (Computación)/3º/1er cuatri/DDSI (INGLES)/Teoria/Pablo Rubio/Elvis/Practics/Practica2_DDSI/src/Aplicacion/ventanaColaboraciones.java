    private void DibujarTablaColabora() {
        tColabora.setModel(modeloTColabora);
        String[] columnasTabla = {"Código","Nombre","Especialiad","Colabracion"};
        modeloTColabora.setColumnIdentifiers(columnasTabla);
        // Para no permitir el redimensionamiento de las columnas con el ratón
        tColabora.getTableHeader().setResizingAllowed(false);
        // Así se fija el ancho de las columnas
        tColabora.getColumnModel().getColumn(0).setPreferredWidth(25);
        tColabora.getColumnModel().getColumn(1).setPreferredWidth(140);
        tColabora.getColumnModel().getColumn(2).setPreferredWidth(80);
        tColabora.getColumnModel().getColumn(3).setPreferredWidth(122);
}
    
private void rellenarTablaColabora (ResultSet rs) {
    Object[] columna = new Object[5];
        try {
            while(rs.next())
            {
                columna[0] = rs.getString("CODEXPERTO");
                columna[1] = rs.getString("NOMBRE");
                columna[2] = rs.getString("ESPECIALIDAD");
                columna[3] = rs.getString("DESCRIPCION_COLABORACION");
                modeloTColabora.addRow(columna);
            }   } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Rellenar las tablas: "+ex.getMessage());
        }
}



private void vaciarTablaColabora(){
    int i = modeloTColabora.getRowCount();
    while (modeloTColabora.getRowCount() > 0)
    modeloTColabora.removeRow(0);
}
    

private void pideColaborador() throws SQLException {
    String colaboradorSeleccionado = txtColaborador.getText();
    ResultSet rs=mCol.listaColaboradoresPorCaso(colaboradorSeleccionado);
    rellenarTablaColabora (rs);
}


