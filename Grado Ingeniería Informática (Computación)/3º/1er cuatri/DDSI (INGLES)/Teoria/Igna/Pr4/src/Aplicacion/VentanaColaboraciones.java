    public void rellenarTablaColabora(ResultSet rs){
        try{
        Object[] fila = new Object[4];
        while(rs.next())
            {
                fila[0] = rs.getString("CODEXPERTO");
                fila[1] = rs.getString("NOMBRE");
                fila[2] = rs.getString("ESPECIALIDAD");
                fila[3] = rs.getString("DESCRIPCION_COLABORACION");
                modeloTColabora.addRow(fila);
            }
        } catch (SQLException e) {
            Logger.getLogger(VentanaColaboraciones.class.getName()).log(Level.SEVERE, null, e);
        }
    }
        
