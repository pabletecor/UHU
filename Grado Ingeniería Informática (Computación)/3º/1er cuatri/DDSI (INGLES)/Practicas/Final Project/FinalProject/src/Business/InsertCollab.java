    private void drawCollabTable() {

        CollabTable.setModel(TExpertLook);
        String[] columnasTabla = {"CodExpert", "CodCaso", "DateC", "Description"};
        TExpertLook.setColumnIdentifiers(columnasTabla);
        // Para no permitir el redimensionamiento de las columnas con el ratón
        CollabTable.getTableHeader().setResizingAllowed(false);
        // Así se fija el ancho de las columnas
        CollabTable.getColumnModel().getColumn(0).setPreferredWidth(25);
        CollabTable.getColumnModel().getColumn(1).setPreferredWidth(140);
        CollabTable.getColumnModel().getColumn(2).setPreferredWidth(80);
        CollabTable.getColumnModel().getColumn(3).setPreferredWidth(122);
    }

    private void fillCollabTable(ArrayList<Collaborates> collab) {    //Cambiarlo para arraylsit de Collaborates
        Object[] column = new Object[4];
        int numRegistros = collab.size();

        for (int i = 0; i < numRegistros; i++) {
            column[0] = collab.get(i).getCodExperto();
            column[1] = collab.get(i).getCodCaso();
            column[2] = collab.get(i).getFecha();
            column[3] = collab.get(i).getDescripcionColaboracion();
            TExpertLook.addRow(column);
        }
    }

    private void deleteCollabTable() {

        int i = TExpertLook.getRowCount();
        while (TExpertLook.getRowCount() > 0) {
            TExpertLook.removeRow(0);
        }
    }

    private void viewCollabs() throws SQLException {

        ArrayList<Collaborates> ColabL = Colab.CollaboratesList();
        fillCollabTable(ColabL);

    }

