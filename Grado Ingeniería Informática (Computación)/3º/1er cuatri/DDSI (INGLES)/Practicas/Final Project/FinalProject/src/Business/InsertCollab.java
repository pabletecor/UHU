    private void CheckExpertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckExpertActionPerformed
        // TODO add your handling code here:
        codExp = CodExpTxt.getText();

        try {
            if (Exp.expertExists(codExp) == true) {  //If the expert does exist, a OK is shown (in green?)
                expLabel.setForeground(Color.blue);
                expLabel.setText("OK");

            } else {
                expLabel.setForeground(Color.RED);
                expLabel.setText("X");
                //Abrimos un nuevo frame para introducir el experto
                try {

                    InsertExpert iExp = new InsertExpert(co,codExp);
                    iExp.setVisible(true);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                } finally {
                    //this.dispose();           Cierra la ventana
                }
            
            }

        } catch (SQLException ex) {
            Logger.getLogger(InsertCollab.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_CheckExpertActionPerformed

    private void CheckCaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckCaseActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            codCase = CodCaseTxt.getText();
            
            if(Pc.existeCaso(codCase) == true){
                caseLabel.setForeground(Color.blue);
                caseLabel.setText("OK");
            }
            else{
                
                caseLabel.setForeground(Color.RED);
                caseLabel.setText("X");
                
                try {
                    InsertCase nC = new InsertCase(co,codCase);
                    nC.setVisible(true);
                } catch (SQLException ex) {
                    caseLabel.setText("Error SQLException checkcase");
                    Logger.getLogger(InsertCollab.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
        } catch (SQLException ex) {
             caseLabel.setText("Error SQLException 2 checkcase");
            Logger.getLogger(InsertCollab.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_CheckCaseActionPerformed

